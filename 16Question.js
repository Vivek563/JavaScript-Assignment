// 16. Get radius using prompt and calculate the area of a circle (area = pi x r x r) and
// circumference of a circle(c = 2 x pi x r) where pi = 3.14.

let pi = 3.14;

let radius = prompt("Enter area of circle");

console.log(`area of circle is ${pi * radius * radius}`);

console.log(`circumference of circle is ${2 * pi * radius}`);
